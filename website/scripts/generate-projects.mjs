/**
 * Reads public/projects/ folders and generates src/data/projects.ts
 *
 * Each folder under public/projects/ that contains a project.json is a project.
 * - Folder name = project slug (used for URL paths)
 * - project.json = { title, category, description }
 * - Image files (jpg/jpeg/png/webp) sorted alphabetically = project images
 *
 * Run: node scripts/generate-projects.mjs
 */

import fs from 'fs';
import path from 'path';

const PROJECTS_DIR = path.resolve('public/projects');
const OUTPUT_FILE = path.resolve('src/data/projects.ts');

const IMAGE_EXTENSIONS = new Set(['.jpg', '.jpeg', '.png', '.webp']);

function getProjectFolders() {
  return fs
    .readdirSync(PROJECTS_DIR, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .filter((name) => {
      const configPath = path.join(PROJECTS_DIR, name, 'project.json');
      return fs.existsSync(configPath);
    })
    .sort();
}

function readProject(folderName) {
  const folderPath = path.join(PROJECTS_DIR, folderName);
  const configPath = path.join(folderPath, 'project.json');
  const config = JSON.parse(fs.readFileSync(configPath, 'utf-8'));

  const images = fs
    .readdirSync(folderPath)
    .filter((f) => IMAGE_EXTENSIONS.has(path.extname(f).toLowerCase()))
    .sort()
    .map((f) => `/projects/${folderName}/${f}`);

  return {
    slug: folderName,
    title: config.title,
    category: config.category,
    description: config.description || '',
    images,
  };
}

function generate() {
  const folders = getProjectFolders();
  const projects = folders.map(readProject);

  console.log(`Found ${projects.length} projects:\n`);
  for (const p of projects) {
    console.log(`  ${p.category.padEnd(14)} ${p.title}`);
    for (const img of p.images) {
      console.log(`                 └─ ${img}`);
    }
  }

  const ts = `// Auto-generated by scripts/generate-projects.mjs
// Run: node scripts/generate-projects.mjs

export interface Project {
  id: number;
  slug: string;
  title: string;
  category: 'Design' | 'Illustration';
  description: string;
  images: string[];
}

export const projects: Project[] = ${JSON.stringify(
    projects.map((p, i) => ({ id: i + 1, ...p })),
    null,
    2,
  )};
`;

  fs.writeFileSync(OUTPUT_FILE, ts, 'utf-8');
  console.log(`\nWrote ${OUTPUT_FILE}`);
}

generate();
